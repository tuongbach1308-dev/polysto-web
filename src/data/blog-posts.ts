export function getBlogPosts(category?: string, page = 1, perPage = 6): { posts: BlogPost[]; total: number } {
  let filtered = blogPosts;
  if (category && category !== 'all') {
    filtered = blogPosts.filter((p) => p.category === category);
  }
  const total = filtered.length;
  const start = (page - 1) * perPage;
  return { posts: filtered.slice(start, start + perPage), total };
}

export function getBlogPostBySlug(slug: string): BlogPost | undefined {
  return blogPosts.find((p) => p.slug === slug);
}

export function getRelatedBlogPosts(postId: string, limit = 3): BlogPost[] {
  const post = blogPosts.find((p) => p.id === postId);
  if (!post) return [];
  return blogPosts.filter((p) => p.category === post.category && p.id !== postId).slice(0, limit);
}
